#import "NSString+LSDObjcSugar.h"


@implementation NSString (LSDObjcSugar)

+(CGRect)lsd_stringBoundsWithTitleString:(NSString *)String andFontOfSize:(CGFloat)fontSize  rectSize:(CGSize)rectSize{

    //获取文字字符串
    NSString *titleString = String;
    //设置文字的属性
    NSDictionary *stringAttribute = @{NSFontAttributeName : [UIFont systemFontOfSize:fontSize]};
    //获取文字的bounds
    CGRect bound =  [titleString boundingRectWithSize:rectSize options:NSStringDrawingUsesLineFragmentOrigin attributes:stringAttribute context:nil];
    //返回文字的bounds
    return  bound;
}



+(NSAttributedString *)lsd_stringWithAttribute:(NSString *)string andSelectedRange:(NSRange)selectedRange andFontSize:(CGFloat)fontSize andColor:(UIColor *)color{
    
    //创建一个可变的属性字符串
    NSMutableAttributedString *attString = [[NSMutableAttributedString alloc]initWithString:string];
    
    //创建一个字典存储字体的属性
    NSDictionary *dict = @{NSFontAttributeName:[UIFont boldSystemFontOfSize:fontSize],NSForegroundColorAttributeName:color};
    
    //在这个范围内设置字体属性
    [attString setAttributes:dict range:selectedRange];
    
    //返回这个属性字符串
    return [attString copy];
}


